#ifndef CORE_TYPES_INCLUDE_TIME_STAMPER_TYPES_H_
#define CORE_TYPES_INCLUDE_TIME_STAMPER_TYPES_H_

#include <cstddef>

namespace time_stamper {

//! \brief Definition of the <b>Boolean</b> type.
typedef bool BooleanType;

//! \brief Definition of the unsigned <b>Byte</b> type.
typedef unsigned char UnsignedByteType;

//! \brief Definition of the signed <b>Byte</b> type.
typedef signed char SignedByteType;

//! \brief Definition of the unsigned <b>Integer</b> type.
typedef uint64_t UnsignedIntegerType;

//! \brief Definition of the signed <b>Integer</b> type.
typedef int64_t SignedIntegerType;

}  // namespace time_stamper

#endif  // CORE_TYPES_INCLUDE_TIME_STAMPER_TYPES_H_
